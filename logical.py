#LOGICAL OPERATORS are used in conditional statements as follows:
a = True
b = False

print (a and b) #prints true only if the value of BOTH a and b is true
print (a or b) #prints true if the value of EITHER a or b is true
print (not b) #prints true if the value of a is false and vice versa
print (not a) #prints true if the value of b is false and vice versa